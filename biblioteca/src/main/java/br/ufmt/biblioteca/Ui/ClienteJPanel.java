    private void salvarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarjButtonActionPerformed
        // TODO add your handling code here:
        String id = idJTextField.getText();
        String nome = nomejTextField.getText();
        String cpf = cpfjFormattedTextField.getText();
        String telefone = telefonejFormattedTextField.getText();
        String email = emailjTextField.getText();
        
        ClienteDTO dto = new ClienteDTO(id, cpf ,nome , email,telefone );
        
        // depois que alteramos ele baguncou mais ainda, antes tava trocando s√≥ o email e telefone
        ClienteService service = new ClienteServiceImpl();
        try {
            service.salvar(dto);
            JOptionPane.showMessageDialog(this, "Salvo com Sucesso!");
            cancelarjButtonActionPerformed(evt);
            atualizarTabela();
        } catch(Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }
    }//GEN-LAST:event_salvarjButtonActionPerformed

