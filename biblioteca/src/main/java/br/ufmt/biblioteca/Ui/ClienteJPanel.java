    private void editarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarjButtonActionPerformed
        // TODO add your handling code here:
        int total = tabelajTable.getSelectedRowCount();
        
        if(total != 1){
            JOptionPane.showMessageDialog(tabelajTable, "Por favor selecione somente uma linha para editar");
            return;
        } 
        int linha = tabelajTable.getSelectedRow();
         String id = (String)tabelajTable.getValueAt(linha, 0);
         String nome = (String)tabelajTable.getValueAt(linha, 1);
         String cpf = (String)tabelajTable.getValueAt(linha, 2);
         String telefone = (String)tabelajTable.getValueAt(linha, 3);
         String email = (String)tabelajTable.getValueAt(linha, 4);
         
         idJTextField.setText(id);
         nomejTextField.setText(nome);
         cpfjFormattedTextField.setText(cpf);
         emailjTextField.setText(email);
         telefonejFormattedTextField.setValue(telefone);
    }//GEN-LAST:event_editarjButtonActionPerformed

    private void deletarjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletarjButtonActionPerformed
        // TODO add your handling code here:
        int total = tabelajTable.getSelectedRowCount();
        if(total <= 0){
            JOptionPane.showMessageDialog(tabelajTable, "Por favor selecione as linhas a serem enviadas");
            return;
        }
        try {
            int[] linhas = tabelajTable.getSelectedRows();
            ClienteService service = new ClienteServiceImpl();
            for(int linha: linhas) {
                String id = (String)tabelajTable.getValueAt(linha, 0);
                ClienteDTO dto = new ClienteDTO(id, null, null, null, null);
                service.remover(dto);
            }
            JOptionPane.showMessageDialog(tabelajTable, "Registro removidos com sucesso!");
            atualizarTabela();
        }catch (Exception erro){
             JOptionPane.showMessageDialog(tabelajTable, erro.getMessage());
        }
    }//GEN-LAST:event_deletarjButtonActionPerformed


