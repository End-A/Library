    private void atualizarTabela() {
        ClienteService service = new ClienteServiceImpl();
        List<ClienteDTO> lista = service.listar();
        
        DefaultTableModel dfm = (DefaultTableModel) tabelajTable.getModel();
        int linhas = dfm.getRowCount();
        for (int i = 0; i < linhas; i++) {
            dfm.removeRow(0);
        }
        
        for (ClienteDTO clienteDTO : lista) {
            Object[] linha = new Object[5];
            linha[0] = clienteDTO.getId();
            linha[1] = clienteDTO.getNome();
            linha[2] = clienteDTO.getCpf();
            linha[3] = clienteDTO.getTelefone();
            linha[4] = clienteDTO.getEmail();
            
            dfm.addRow(linha);
        }
    }
