    private void atualizarTabela() {
       AutorService service = new AutorServiceImpl();
        List<AutorDTO> lista = service.listar();
        
        DefaultTableModel dfm = (DefaultTableModel) tabelajTable.getModel();
        int linhas = dfm.getRowCount();
        for (int i = 0; i < linhas; i++) {
            dfm.removeRow(0);
        }
 
        
        for (AutorDTO  autorDTO  : lista) {
            Object[] linha = new Object[3];
            linha[0] = autorDTO.getId();
            linha[1] = autorDTO.getNome();
            linha[2] = autorDTO.getBiografia();
 
            
            dfm.addRow(linha);
        }
    }
